# more conditionals practice

print("\nStrings:")
print(f"true != false: {'true' != 'false'}")
print(f"true == false: {'true' == 'false'}")

print("\nLower():")
print(f"True != True.lower(): {'True' != 'True'.lower()}")

print("\nNumerical Equality:")
print(f"3 == 4: {3 == 4}")
print(f"3 != 4: {3 != 4}")

print("\n> / < / >= / <= :")
print(f"3 < 4: {3 < 4}")
print(f"3 > 4: {3 > 4}")
print(f"3 <= 4: {3 <= 4}")
print(f"3 >= 4: {3 >= 4}")

print("\nAND / OR :")
print(f"True and False: {True and False}")
print(f"True or False: {True or False}")

print("\nin or not in [list] :")
print(f"1 in range(1,3): {1 in range(1,3)}")
print(f"3 in range(1,3): {3 in range(1,3)}")