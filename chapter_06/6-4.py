# Glossary

glossary = {
    'loop' : 'a way of repeating code',
    'string' : 'a variable type used to store ASCII characters',
    'dictionary' : 'a list where the indices (called keys) are named',
    'operator' : 'One of the arithmetic or conditional symbols, such as "<", "or", etc',
    'variable' : 'a way to store and access a value'
}

for key in glossary:
    print(f"{key}: {glossary[key]}.")