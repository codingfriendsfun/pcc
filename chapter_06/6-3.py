#index
index = {
    'list': 'An array.',
    'title()': 'Capitalizes the first letters of each word.',
    'upper()': 'Converts all letters to uppercase.',
    'lower()': 'Converts all letters to lowercase.',
    'strip()': 'Removes beginning and trailing whitespace.',
    }

print('List:')
print(f"\t{index['list']}")

print('\ntitle():')
print(f"\t{index['title()']}")

print('\nupper():')
print(f"\t{index['upper()']}")

print('\nlower():')
print(f"\t{index['lower()']}")

print('\nstrip():')
print(f"\t{index['strip()']}")
